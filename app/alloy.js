// Set Enviroment to local or live 
var ENV = "live";

// Alloy.Globals.WEBAPP set the website URI to load
if (ENV == "local") {
	Alloy.Globals.WEBAPP = 'http://localhost/';
} else {
	Alloy.Globals.WEBAPP = 'https://google.co.za';
}

/*
 ---------- ---------- ---------- ---------- ---------- ---------- ---------- ---------- ----------
 ---------- ---------- ---------- ---------- ---------- ---------- ---------- ---------- ----------
 ---------- ----- DEFINE CONTROLLER TO CREATE AND OPEN Alloy.Globals.WEBAPP ---------- -----
 ---------- ------ open controllers/index.js ---------- ----------
 ---------- ---------- ---------- ---------- ---------- ---------- ---------- ---------- ----------
 */
var openWebviewsView = Alloy.createController('index', {
	title : 'Website title',
	url : Alloy.Globals.WEBAPP + '/'
}).getView().open();

